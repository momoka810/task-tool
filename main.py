def load_api_key():
    """
    APIキーをファイルから読み込む
    優先順位: ファイル > 環境変数
    """
    # まずファイルから読み込みを試みる
    api_key_file = os.path.join(os.path.dirname(__file__), API_KEY_FILE)
    if os.path.exists(api_key_file):
        try:
            with open(api_key_file, 'r', encoding='utf-8') as f:
                api_key = f.read().strip()
                # OPENAI_API_KEY= という形式で記入されている場合、=以降を取得
                if api_key.startswith('OPENAI_API_KEY='):
                    api_key = api_key.split('=', 1)[1].strip()
                if api_key:
                    return api_key
        except Exception as e:
            print(f"【警告】APIキーファイルの読み込みに失敗しました: {e}")
    
    # ファイルがない場合は環境変数から取得
    api_key = os.getenv("OPENAI_API_KEY", "")
    if api_key:
        return api_key
    
    # どちらもない場合は空文字列を返す
    return ""

