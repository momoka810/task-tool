def calculate_weighted_score(task):
    """
    重み付きスコアを計算する
    
    評価軸：
    - 将来性（高いほど良い）
    - 今の自分とのギャップ（小さいほど高評価 → 6-ギャップで計算）
    - 習得コスト（低いほど高評価 → 6-コストで計算）
    - 汎用性（高いほど良い）
    - 心理的ハードル（低いほど高評価 → 6-ハードルで計算）
    
    重み付け：
    - 汎用性と習得コストの低さを重視（成功体験を作りやすい）
    - 心理的ハードルの高さをペナルティとして適用
    """
    # 各評価軸のスコア（1〜5点）
    将来性 = task['将来性']
    ギャップ = task['今の自分とのギャップ']
    習得コスト = task['習得コスト']
    汎用性 = task['汎用性']
    心理的ハードル = task['心理的ハードル']
    
    # 逆転が必要な軸（小さいほど高評価）を変換
    # 1点→5点、2点→4点、...、5点→1点（6-値で計算）
    ギャップ_スコア = 6 - ギャップ
    習得コスト_スコア = 6 - 習得コスト
    心理的ハードル_スコア = 6 - 心理的ハードル
    
    # 重み付け（合計が意味のある値になるように調整）
    # 汎用性と習得コストの低さを重視
    重み_将来性 = 1.0
    重み_ギャップ = 0.8
    重み_習得コスト = 1.5  # 習得コストが低いほど優先
    重み_汎用性 = 1.5  # 汎用性が高いほど優先
    重み_心理的ハードル = 1.2  # 心理的ハードルが低いほど優先
    
    # 重み付きスコア計算
    総合スコア = (
        将来性 * 重み_将来性 +
        ギャップ_スコア * 重み_ギャップ +
        習得コスト_スコア * 重み_習得コスト +
        汎用性 * 重み_汎用性 +
        心理的ハードル_スコア * 重み_心理的ハードル
    )
    
    return 総合スコア


def generate_reason(task, score):
    """
    タスクの優先順位の理由を生成する
    """
    理由リスト = []
    
    # 汎用性が高い場合
    if task['汎用性'] >= 4:
        理由リスト.append("汎用性が高い")
    
    # 習得コストが低い場合
    if task['習得コスト'] <= 2:
        理由リスト.append("習得コストが低い")
    
    # 心理的ハードルが低い場合
    if task['心理的ハードル'] <= 2:
        理由リスト.append("心理的ハードルが低い")
    
    # 将来性が高い場合
    if task['将来性'] >= 4:
        理由リスト.append("将来性が高い")
    
    # ギャップが小さい場合
    if task['今の自分とのギャップ'] <= 2:
        理由リスト.append("今の自分とのギャップが小さい")
    
    # ネガティブな要因
    ネガティブ理由 = []
    if task['心理的ハードル'] >= 4:
        ネガティブ理由.append("心理的ハードルが高い")
    if task['習得コスト'] >= 4:
        ネガティブ理由.append("習得コストが高い")
    if task['今の自分とのギャップ'] >= 4:
        ネガティブ理由.append("今の自分とのギャップが大きい")
    
    # 理由を組み立て
    if 理由リスト:
        理由 = "、".join(理由リスト) + "ため今すぐ着手しやすい"
    else:
        理由 = "総合的に判断して優先度が高い"
    
    if ネガティブ理由:
        理由 += "（ただし、" + "、".join(ネガティブ理由) + "点に注意）"
    
    return 理由


def prioritize_tasks(tasks, future_vision="", current_situation=""):
    """
    タスクの優先順位を計算し、ソートする
    
    Args:
        tasks: タスクのリスト
        future_vision: ユーザーの将来像
        current_situation: ユーザーの現在の状況
    """
    # AI評価を実行
    print("=" * 60)
    print("AI評価を実行中...")
    print("=" * 60)
    print()
    
    for i, task in enumerate(tasks, 1):
        print(f"  {i}/{len(tasks)}: {task['タスク名']} を評価中...")
        try:
            # AI評価を実行
            ai_evaluation = evaluate_task_with_ai(
                task['タスク名'],
                task.get('説明', ''),
                future_vision,
                current_situation
            )
            
            # AI評価結果を設定
            task['将来性'] = ai_evaluation['将来性']
            task['今の自分とのギャップ'] = ai_evaluation['今の自分とのギャップ']
            task['習得コスト'] = ai_evaluation['習得コスト']
            task['汎用性'] = ai_evaluation['汎用性']
            task['心理的ハードル'] = ai_evaluation['心理的ハードル']
            
            print(f"    ✓ 評価完了: 将来性={ai_evaluation['将来性']}, ギャップ={ai_evaluation['今の自分とのギャップ']}, "
                  f"コスト={ai_evaluation['習得コスト']}, 汎用性={ai_evaluation['汎用性']}, "
                  f"ハードル={ai_evaluation['心理的ハードル']}")
        except ImportError as e:
            print(f"    ✗ エラー: {e}")
            raise
        except ValueError as e:
            print(f"    ✗ エラー: {e}")
            raise
        except Exception as e:
            print(f"    ✗ エラー: {type(e).__name__}: {e}")
            error_msg = f"タスク '{task['タスク名']}' の評価に失敗しました。\n"
            error_msg += f"エラー詳細: {str(e)}\n\n"
            error_msg += "確認事項:\n"
            error_msg += "1. openaiライブラリがインストールされているか: pip install openai\n"
            error_msg += f"2. api_key.txtファイルにAPIキーが正しく記入されているか\n"
            error_msg += "3. APIキーが有効か（OpenAIのダッシュボードで確認）\n"
            error_msg += "4. インターネット接続が正常か"
            raise RuntimeError(error_msg)
    print()
    
    # 各タスクにスコアを追加
    for task in tasks:
        # 必須フィールドのチェック
        required_fields = ['将来性', '今の自分とのギャップ', '習得コスト', '汎用性', '心理的ハードル']
        missing_fields = [field for field in required_fields if field not in task]
        
        if missing_fields:
            raise ValueError(f"タスク '{task.get('タスク名', '不明')}' に以下の評価値が不足しています: {', '.join(missing_fields)}")
        
        task['総合スコア'] = calculate_weighted_score(task)
        task['理由'] = generate_reason(task, task['総合スコア'])
    
    # 総合スコアの降順でソート
    sorted_tasks = sorted(tasks, key=lambda x: x['総合スコア'], reverse=True)
    
    return sorted_tasks


